<h4>Welcome to Light-php</h4>

<p>
	Simple MVC framework done in PHP, made to be the most simple possible and easy to understand!
	<br><br>
	You can start modifying this site to make it your personal project!, as easy as change the HTML in <span class="code">src/view/template/info/welcomeView.php</span> and then the <span class="code">Controller</span> plus the <span class="code">Model</span> files to give it the result you want.
	<br><br>
	Don't forget to see the documentation for <a target="_blank" href="https://bakeiro.github.io/Light-PHP-documentation/">help</a>
	<br><br>
	Important: This default site is a showcase of how to do an app with my microframework, it doesn't pretend to be a full-featured site, you can use it to understand how the PHP framework works.
</p>
<br>


